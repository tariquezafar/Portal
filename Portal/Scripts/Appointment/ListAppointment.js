function SearchAppointments() {
    var txtAppointLetterNo = $("#txtAppointLetterNo");
    var txtInterviewNo = $("#txtInterviewNo");
    var txtApplicantName = $("#txtApplicantName");
    var ddlAppointStatus = $("#ddlAppointStatus");
    var txtFromDate = $("#txtFromDate");
    var txtToDate = $("#txtToDate");
    var ddlCompanyBranch= $("#ddlCompanyBranch");
    var requestData = {
        appointLetterNo: txtAppointLetterNo.val().trim(), interviewNo: txtInterviewNo.val(), applicantName: txtApplicantName.val(), appointStatus: ddlAppointStatus.val(), fromDate: txtFromDate.val(), toDate: txtToDate.val(),companyBranch: ddlCompanyBranch.val()
    };
    $.ajax({
        url: "../Appointment/GetAppointmentList",
        data: requestData,
        dataType: "html",
        type: "GET",
        error: function (err) {
            $("#divList").html("");
            $("#divList").html(err);
        },
        success: function (data) {
            $("#divList").html("");
            $("#divList").html(data);
        }
    });
}
