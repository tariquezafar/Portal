function SearchCalender() {
    var txtCalenderName = $("#txtCalenderName");
    var ddlCalenderYear = $("#ddlCalenderYear");
    var ddlStatus = $("#ddlStatus");
    
    var requestData = { calenderName: txtCalenderName.val().trim(), calenderYear: ddlCalenderYear.val(), calenderStatus: ddlStatus.val() };
    $.ajax({
        url: "../Calender/GetCalenderList",
        data: requestData, 
        dataType: "html",
        type: "GET",
        error: function (err) {
            $("#divList").html("");
            $("#divList").html(err);
        },
        success: function (data) {
            $("#divList").html("");
            $("#divList").html(data); 
        }
    });
}
