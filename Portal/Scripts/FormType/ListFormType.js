function SearchFormType() {
    var txtFormTypeDesc = $("#txtFormTypeDesc");
    var hdnFormTypeId = $("#hdnFormTypeId");
    var ddlStatus = $("#ddlStatus");
    var requestData = {
        FormTypeDesc: txtFormTypeDesc.val().trim(),
        Status: ddlStatus.val()
    };

    $.ajax({
        url: "../FormType/GetFormTypeList",
        data: requestData,
        dataType: "html",
        type: "GET",
        error: function (err) {
            $("#divList").html("");
            $("#divList").html(err);
        },
        success: function (data) {
            $("#divList").html("");
            $("#divList").html(data);

        }
    });
}
