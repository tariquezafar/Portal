function SearchManufacturer() {
    var txtManufacturerName = $("#txtManufacturerName");
    var txtManufacturerCode = $("#txtManufacturerCode");
    var ddlStatus = $("#ddlStatus");
    
    var requestData = { manufacturerName: txtManufacturerName.val().trim(), manufacturerCode: txtManufacturerCode.val().trim(), manufacturerStatus: ddlStatus.val() };
    $.ajax({
        url: "../Manufacturer/GetManufacturerList",
        data: requestData, 
        dataType: "html",
        type: "GET",
        error: function (err) {
            $("#divList").html("");
            $("#divList").html(err);
        },
        success: function (data) {
            $("#divList").html("");
            $("#divList").html(data); 
        }
    });
}
