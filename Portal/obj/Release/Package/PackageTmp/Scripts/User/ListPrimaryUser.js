function ClearFields()
{
    $("#txtUserName").val("");
    $("#txtPhoneNo").val("");
    $("#txtFullName").val("");
    $("#txtEmail").val("");
    $("#ddlCompany").val("0");

}
function SearchUser() {

    var txtUserName = $("#txtUserName");
    var txtPhoneNo = $("#txtPhoneNo");
    var txtFullName = $("#txtFullName");
    var txtEmail = $("#txtEmail");
    var ddlCompany = $("#ddlCompany");

    var requestData = { userName: txtUserName.val().trim(), phoneNo: txtPhoneNo.val().trim(), fullName: txtFullName.val().trim(), email: txtEmail.val().trim(), companyId: ddlCompany.val() };
    $.ajax({
        url: "../User/GetPrimaryUserList",
        data: requestData,
        dataType: "html",
        type: "GET",
        error: function (err) {
            $("#divList").html("");
            $("#divList").html(err);
        },
        success: function (data) {
            $("#divList").html("");
            $("#divList").html(data);

        }
    });
}
