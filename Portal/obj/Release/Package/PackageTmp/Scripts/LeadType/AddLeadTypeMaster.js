$(document).ready(function () {
    BindCompanyBranchList();
    var hdnAccessMode = $("#hdnAccessMode");
    var hdnLeadId = $("#hdnLeadId");
    if (hdnLeadId.val() != "" && hdnLeadId.val() != "0" && hdnAccessMode.val() != "" && hdnAccessMode.val() != "0") {
        GetLeadDetails(hdnLeadId.val());
        if (hdnAccessMode.val() == "3") {
            $("#btnSave").hide();
            $("#btnUpdate").hide();
            $("#btnReset").hide();
            $("input").attr('readOnly', true);
            $("textarea").attr('readOnly', true);
            $("select").attr('disabled', true);
            $("#chkstatus").attr('disabled', true);
        }
        else {
            $("#btnSave").hide();
            $("#btnUpdate").show();
            $("#btnReset").show();
        }
    }
    else {
        $("#btnSave").show();
        $("#btnUpdate").hide();
        $("#btnReset").show();
    }
    
    
     
});


function BindCompanyBranchList() {
    $("#ddlCompanyBranch").val(0);
    $("#ddlCompanyBranch").html("");
    $.ajax({
        type: "GET",
        url: "../DeliveryChallan/GetCompanyBranchList",
        data: {},
        dataType: "json",
        asnc: false,
        success: function (data) {
            $("#ddlCompanyBranch").append($("<option></option>").val(0).html("-Select Company Branch-"));
            $.each(data, function (i, item) {
                $("#ddlCompanyBranch").append($("<option></option>").val(item.CompanyBranchId).html(item.BranchName));
            });
            var hdnSessionCompanyBranchId = $("#hdnSessionCompanyBranchId");
            var hdnSessionUserID = $("#hdnSessionUserID");

            if (hdnSessionCompanyBranchId.val() != "0" && hdnSessionUserID.val() != "2") {
                $("#ddlCompanyBranch").val(hdnSessionCompanyBranchId.val());
                $("#ddlCompanyBranch").attr('disabled', true);
            }
        },
        error: function (Result) {
            $("#ddlCompanyBranch").append($("<option></option>").val(0).html("Select Company Branch"));
        }
    });
}


function GetLeadDetails(LeadId) {
   
    $.ajax({
        type: "GET",
        asnc: false,
        url: "../LeadMaster/GetLeadMasterDetails",
        data: { LeadTypeId: LeadId },
        dataType: "json",
        success: function (data) {
            $("#txtLeadTypeMaster").val(data.LeadTypeName); 
            $("#ddlCompanyBranch").val(data.CompanyBranchid);
            
        },
        error: function (Result) {
            ShowModel("Alert", "Problem in Request");
        }
    });
}



function SaveData() {
    
    var hdnLeadId = $("#hdnLeadId");
    var txtLeadTypeMaster = $("#txtLeadTypeMaster");
    var chkstatus = $("#chkstatus");
    var ddlCompanyBranch = $("#ddlCompanyBranch");
   
    var leadchkeStatus = true;
    if (chkstatus.prop("checked") == true)
    { leadchkeStatus = true; }
    else
    { leadchkeStatus = false; }

    if (txtLeadTypeMaster.val().trim() == "") {
         ShowModel("Alert", "Please Enter Lead Name")
         txtLeadTypeMaster.focus();
        return false;
    }
    if (ddlCompanyBranch.val() == "0") {
        ShowModel("Alert", "Please Select Company Branch")
        ddlCompanyBranch.focus();
        return false;
    }


   
  
  
    var leadTypeMasterViewModel = {
        LeadTypeId: hdnLeadId.val(),
        LeadTypeName: txtLeadTypeMaster.val().trim(),
        CompanyBranchid: ddlCompanyBranch.val(),
        status: leadchkeStatus,
       
    };

    var accessMode = 1;//Add Mode
    if (hdnLeadId.val() != null && hdnLeadId.val() != 0) {
        accessMode = 2;//Edit Mode
    }
    
    var requestData = { leadTypeMasterViewModel: leadTypeMasterViewModel };
  
    $.ajax({
        url: "../LeadMaster/AddEditLeadMaster?accessMode=" + accessMode + "",
        cache: false,
        type: "POST",
        dataType: "json",
        data: JSON.stringify(requestData),
        contentType: 'application/json',
        success: function (data) {
            if (data.status == "SUCCESS") {
                ShowModel("Alert", data.message);
                ClearFields();
                setTimeout(
                  function () {
                      window.location.href = "../LeadMaster/ListLeadMaster";
                  }, 2000);
                $("#btnSave").show();
                $("#btnUpdate").hide();
            }
            else {
                ShowModel("Error", data.message)
            }
        },
        error: function (err) {
            ShowModel("Error", err)
        }
    });



}
$(".alpha-only").on("input", function () {
    var regexp = /[^a-zA-Z]/g;
    if ($(this).val().match(regexp)) {
        $(this).val($(this).val().replace(regexp, ''));
    }
});
